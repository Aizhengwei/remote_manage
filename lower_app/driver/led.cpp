void led_convert(uint8_t work)
{
    int fd;
    uint8_t val;

    fd = open(LED_DEVICE, O_RDWR | O_NDELAY);
    if(fd != -1)
    {
        val = work;
        write(fd, &val, 1);  //将数据写入LED
        close(fd);
    }
}
